# PEP8: inicie variáveis com letras minúsculas, pode usar números e underline _.
# O sinal de = é o operador de atribuição. Ele é usado para atribuir um valor a variável.
# Uso: nome_variável = expressão

nome_completo = "Antonio Carlos Lima Neves"
