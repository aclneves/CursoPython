# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número positivo ou negativo. int sem sinal é positivo
print(11)
print(-11)
print(0)

# float -> Número com ponto flutuante
# O tipo float representar qualquer número positivo ou negarivo com ponto flutuante,
# float sem sinal é positivo
print(1.1, 10.11)
print(0.0, -1.5)

# A função type mostra o tipoe que o Python inferio ao valor
print(type("Antonio"))
print(type(1), type(0))
print(type(1.1), type(0.0))
