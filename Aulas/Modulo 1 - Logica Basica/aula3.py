# Aspas simples
print('Antonio Neves')

# Aspas duplas
print("Antonio Neves")

# Escape
print("Antonio \"Neves\"")
print('Antonio "Neves"') #Macete para usar aspas simples ou aspas duplas como texto

# r
print(r"Antonio \"Neves\"")